    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIncluirActionPerformed
        // TODO add your handling code here:
        Funcionario f ;
        if(jTxtMatricula.getText().isEmpty() || jTxtNome.getText().isEmpty()) 
           Diversos.mostrarDados("Favor preencher os campos", titulo, false);
        else{  
            f = new Funcionario();
            f.setMatricula(Integer.parseInt(jTxtMatricula.getText()));
            f.setNome(jTxtNome.getText());
            f.setSalario(Float.parseFloat(jTxtSalario.getText().replace(",", ".")));
            Cargo c = new Cargo();
            String cargo = (String) jCmbCargo.getSelectedItem();
            c.setNumero(Integer.parseInt(jLblNumero.getText()));
            c.setDescricao(cargo);
            f.setCargo(c);
            f.setAtivo(jChkSim.isSelected());
            if (jBtnIncluir.isEnabled()) { 
                 if (cF.setManipular(f, 'I')) {
                     Diversos.mostrarDados("Funcionario(a) " + jTxtNome.getText() +
                             " foi inserido(a) com sucesso", titulo, true);
                     jBtnAlterar.setEnabled(true);
                     jBtnExcluir.setEnabled(true);
                 }
                 else 
                     Diversos.mostrarDados("Problemas ao inserir os dados do(a) funcionario(a) " +
                                 jTxtNome.getText(), titulo, false); 
                      
                 jBtnIncluir.setEnabled(false);
            }
            else if (cF.setManipular(f, 'A'))
                      Diversos.mostrarDados("Funcionario(a) " + jTxtNome.getText() +
                            " foi atualizado(a) com sucesso", titulo, true);                    
                  else {
                         Diversos.mostrarDados("Problemas ao atualizar os dados do(a) funcionario(a) " +
                                jTxtNome.getText(), titulo, false); 
                        jBtnAlterar.setEnabled(false);
                         jBtnExcluir.setEnabled(false);
                  }            
            }             
    }//GEN-LAST:event_jBtnIncluirActionPerformed

