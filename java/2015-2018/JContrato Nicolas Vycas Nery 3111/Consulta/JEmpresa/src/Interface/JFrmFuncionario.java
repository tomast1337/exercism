    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
       if (Diversos.confirmar("Deseja remover o funcionario(a) ", titulo)){
          Object o = cF.getBusca(Integer.parseInt(jTxtMatricula.getText()), 0);
          Funcionario f = (Funcionario) o;  
          if (cF.setManipular(f, 'E')) {
            Diversos.mostrarDados("Funcionario(a) " + jTxtNome.getText() + 
                  " foi removido com sucesso!!!", titulo, true);
           jBtnLimparActionPerformed(evt);
           jBtnAlterar.setEnabled(false);
           jBtnExcluir.setEnabled(false);   
         
       }
       else
         Diversos.mostrarDados("Problema ao remover o cargo " + 
                 jTxtNome.getText() , titulo, false);
      }                         
    }//GEN-LAST:event_jBtnExcluirActionPerformed

