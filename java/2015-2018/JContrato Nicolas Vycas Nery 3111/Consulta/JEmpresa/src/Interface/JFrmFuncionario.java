    private void jTxtMatriculaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTxtMatriculaFocusLost
        // TODO add your handling code here:
        Object o;
        Funcionario f;
        int matricula;
       
        if (!Diversos.testaNum(jTxtMatricula.getText(), titulo))
              jTxtMatricula.setText("");
        else if (!Diversos.intervalo(Integer.parseInt(jTxtMatricula.getText()) ,0 ,0 , titulo))
                  jTxtMatricula.setText("");
               else {
                      matricula= Integer.parseInt(jTxtMatricula.getText());
                      o =  cF.getBusca(matricula, 0);
                     if (o == null) {
                        jBtnIncluir.setEnabled(true);
                        Diversos.mostrarDados("Funcionario inexistente", titulo, false);
                     }
                else{
                      f = (Funcionario) o;
                      carregaObjetos(f);
                     jBtnAlterar.setEnabled(true);
                     jBtnExcluir.setEnabled(true);
                }
                jTxtMatricula.setEditable(false);
        } 
         
    }//GEN-LAST:event_jTxtMatriculaFocusLost

