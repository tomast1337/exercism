    private void limpar() { 
         JTextField txt[] = {jTxtMatricula, jTxtNome,  jTxtSalario};
        for (JTextField t : txt)
            t.setText("");
        jTxtMatricula.setEditable(true);
        jLblNumero.setText("");
        jCmbCargo.setSelectedIndex(-1);
        buttonGroup.clearSelection();
        jChkSim.setSelected(false);
        jBtnAlterar.setEnabled(false);
        jBtnExcluir.setEnabled(false);
        jBtnIncluir.setEnabled(false);
        jTxtNome.requestFocusInWindow();
    }
    private void carregaObjetos(Funcionario f) {
       jTxtNome.setText(f.getNome());
       jTxtMatricula.setText(String.valueOf(f.getMatricula()));       
       jLblNumero.setText(String.valueOf(f.getCargo().getNumero()));
       jCmbCargo.setSelectedItem(f.getCargo().getDescricao());
       jTxtSalario.setText(NumberFormat.getNumberInstance().format(f.getSalario()));
       jChkSim.setSelected(f.isAtivo());
    } 
    
     private void carregaLista() {
        int posi = jCmbCargo.getSelectedIndex(); //guarda a posição coorente
        listaC = cC.lista();
       jCmbCargo.removeAllItems();
       for (Object o : listaC) {
            Cargo c = (Cargo) o;
            jCmbCargo.addItem(c.getDescricao());
        }
         if (posi > -1) 
              jCmbCargo.setSelectedIndex(posi);
         else {
            jLblNumero.setText("");
            jCmbCargo.setSelectedIndex(-1);
         }
     }     
